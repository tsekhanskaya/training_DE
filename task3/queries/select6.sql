-- 6. Вывести города с количеством активных и неактивных клиентов
-- (активный — customer.active = 1). Отсортировать по количеству неактивных клиентов
-- по убыванию.

SELECT city.city,
       COUNT(CASE WHEN customer.active = 1 THEN 1 END) AS active_customers,
       COUNT(CASE WHEN customer.active = 0 THEN 1 END) AS inactive_customers
FROM customer
JOIN address ON customer.address_id = address.address_id
JOIN city ON address.city_id = city.city_id
GROUP BY city.city
ORDER BY inactive_customers DESC;

